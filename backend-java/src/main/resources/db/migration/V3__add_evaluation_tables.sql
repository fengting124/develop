create table evaluation_run (
    id bigint generated by default as identity primary key,
    evaluation_id varchar(64) not null unique,
    name varchar(255) not null,
    dataset_name varchar(255) not null,
    model_id varchar(64) not null,
    status varchar(32) not null,
    total_samples integer not null,
    completed_samples integer not null,
    accuracy double precision,
    precision_score double precision,
    recall_score double precision,
    f1_score double precision,
    created_at timestamp with time zone not null,
    started_at timestamp with time zone,
    completed_at timestamp with time zone,
    failure_reason varchar(2048),
    constraint fk_evaluation_run_model foreign key (model_id) references model_registry(model_id)
);

create index idx_evaluation_run_status_created_at on evaluation_run(status, created_at);

create table evaluation_sample (
    id bigint generated by default as identity primary key,
    sample_id varchar(64) not null unique,
    evaluation_id varchar(64) not null,
    filename varchar(255) not null,
    ground_truth_label varchar(32) not null,
    predicted_label varchar(32),
    score double precision,
    latency_ms integer,
    correct boolean,
    failure_reason varchar(2048),
    created_at timestamp with time zone not null,
    constraint fk_evaluation_sample_run foreign key (evaluation_id) references evaluation_run(evaluation_id)
);

create index idx_evaluation_sample_evaluation_id on evaluation_sample(evaluation_id);
create index idx_evaluation_sample_evaluation_correct on evaluation_sample(evaluation_id, correct);
